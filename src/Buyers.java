
public class Buyers {

    String name;
    String patronymic;
    String surname;
    int age;
    int phone;



    public Buyers(String name, String patronymic, String surname, int age, int phone) {
        this.name = name;
        this.patronymic = patronymic;
        this.surname = surname;
        this.age = age;
        this.phone = phone;

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPatronymic() {
        return patronymic;
    }

    public void setPatronymic(String patronymic) {
        this.patronymic = patronymic;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }
}
